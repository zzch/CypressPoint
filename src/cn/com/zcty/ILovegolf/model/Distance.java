package cn.com.zcty.ILovegolf.model;

public class Distance {
	private String per_round;
	private String shots_to_hole;
	private String holed_percentage;
	private String dispersion;
	public Distance() {
	
	}
	public String getPer_round() {
		return per_round;
	}
	public void setPer_round(String per_round) {
		this.per_round = per_round;
	}
	public String getShots_to_hole() {
		return shots_to_hole;
	}
	public void setShots_to_hole(String shots_to_hole) {
		this.shots_to_hole = shots_to_hole;
	}
	public String getHoled_percentage() {
		return holed_percentage;
	}
	public void setHoled_percentage(String holed_percentage) {
		this.holed_percentage = holed_percentage;
	}
	public String getDispersion() {
		return dispersion;
	}
	public void setDispersion(String dispersion) {
		this.dispersion = dispersion;
	}
	
}
